umur = int(input("masukan umur anda :"))

if umur <=2 :
    print("anda masih bayi")
elif umur <=5 :
    print("anda masih balita")
elif umur <=12 : 
    print("anda masih anak-anak")
elif umur <=17 :
    print("anda sudah remaja")
elif umur <=30 :
    print("anda sudah dewasa")
else :
    print("anda sudah orangtua")


